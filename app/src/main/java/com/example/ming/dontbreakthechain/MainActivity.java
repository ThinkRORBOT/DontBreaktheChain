    private boolean readFile(){
        File dir = new File(getFilesDir()+"/dontbreakthechain");

        if(!dir.exists()) {
            dir.mkdir();
            Log.d("directory", "made");
        }

        file = new File(dir.getAbsolutePath() + "/habits.txt");

        if(!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException e) {
                e.printStackTrace();
                messageBox("readFile", e.getMessage());
            }
        }
    }
